package com.example.k3bootsecurity.dto;

import javax.validation.constraints.*;

public class UserDto {

    private Long id;

    @Size(min = 2, message = "The minimum length of the name is 2 letters")
    private String name;

    @NotBlank(message = "The last name cannot be empty")
    private String surname;

    @Min(value = 18, message = "The minimum age for registration is 18 years")
    @Max(value = 75, message = "The maximum age for registration is 75 years")
    private Byte age;

    @Email(regexp = "[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*@(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?", message = "Example email: examples@mail.ru")
    private String email;

    public UserDto() {
    }

    public UserDto(Long id, String name, String surname, Byte age, String email) {
        this.id = id;
        this.name = name;
        this.surname = surname;
        this.age = age;
        this.email = email;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public Byte getAge() {
        return age;
    }

    public void setAge(Byte age) {
        this.age = age;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public String toString() {
        return "User{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", surname='" + surname + '\'' +
                ", age=" + age +
                ", email='" + email + '\'' +
                '}';
    }
}
